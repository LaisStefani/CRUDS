    public void exibirDados() {
        DefaultTableModel tabela = (DefaultTableModel) TabelaDiretor.getModel();
        tabela.setNumRows(0);
        Controller_Diretor lista = new Controller_Diretor();
        
        for (Tbl_Diretor p: lista.enviaDados()) {
            tabela.addRow(new Object[]{
              p.getId_diretor(),
              p.getNome_diretor(),
              p.getNacionalidade(),
              p.getDt_nascimento()
        });        
        }
    }

