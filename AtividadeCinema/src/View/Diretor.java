    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Controller.Controller_Diretor d = new Controller_Diretor();
        if ((jTextFieldDiretor.getText().isEmpty()) || (jTextFieldNacional.getText().isEmpty()) || (jTextFieldNasc.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os dados nÃ£o podem retornar vazio");
        } else {
            try {
                d.salvar(Integer.parseInt(txtCodigo.getText()), jTextFieldDiretor.getText(), jTextFieldNacional.getText(),
                        jTextFieldNasc.getText());
            } catch (SQLException | ParseException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
            JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso");
            jTextFieldDiretor.setText("");
            jTextFieldNacional.setText("");
            jTextFieldNasc.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
